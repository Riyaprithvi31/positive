import java.util.*;
class positive
{
 public static void main(String args[])
 {
  Scanner sc=new Scanner(System.in);
  int s=sc.nextInt();
  if(s>0)
  {
    System.out.print("positive");
  }
  else if(s==0)
  {
   System.out.println("zero");
  }
  else
  {
   System.out.println("negative");
   }
  }
}
   
